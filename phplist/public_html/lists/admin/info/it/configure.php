<p>Qu&igrave; devi specificare i corretti valori del tuo sito internet.<br/>
Puoi usare predefiniti <b>Segnaposto</b> nei tuoi valori.
Segnaposto del tipo <b>[SOMETEXT]</b>, dove <i>SOMETEXT</i> puo essere differente.
Alcuni segnaposto utili sono:
<ul>
<li>WEBSITE - l\'indirizzo che hai specificato per il tuo sito
<li>DOMAIN - Il testo che hai specificato per il tuo dominio
<li>SUBSCRIBEURL - la posizione della pagina di sottoscrizione
<li>UNSUBSCRIBEURL - la posizione della pagina di cancellazione sottoscrizione
<li>PREFERENCESURL - la posizione della pagina dove gli utenti possono aggiornare i propri dettagli
<li>CONFIRMATIONURL - la posizione della pagina dove gli utenti possono confermare la loro sottoscrizione
</ul>
