Deze optie staat je toe het systeem automatisch een nieuw bericht aan te maken
dat een exacte kopie is van het momentele vericht, vastgelegd bij het interval dat je aanduid.
Als je deze oprie gebruikt, dan is het belangrijk om de "embargo" optie ook te gebruiken, anders
zal je bericht constant worden verzonden, wat verzenden van veel berichten die hetzelfde zijn naar de gebruikers veroorzaakt.


