<b>Embargo een bericht</b>
<p>Je kan een bericht embargo-en zodat het niet wordt verzonden voor de datum en tijd die jij hebt aangegeven.
Standaard wordt dit op vandaag om 0:00 geplaatst, dus het zal onmiddellijk ingesteld worden. </p>
<p><b>Let op</b>: Het embargo heeft invloed op de tijd dat het verzenden van het bericht begint.
Dit wil niet zeggen dat de berichten echt op dat moment zullen aankomen bij de gebruikers.
</p>